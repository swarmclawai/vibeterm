import type { ITheme } from "@xterm/xterm";

export interface AppThemeUI {
  bg: string;
  chromeBg: string;
  dropdownBg: string;
  border: string;
  inputBorder: string;
  dimText: string;
  mutedText: string;
  foreground: string;
  accent: string;
  selection: string;
  separator: string;
  separatorActive: string;
}

export interface AppTheme {
  name: string;
  custom?: boolean;
  terminal: ITheme;
  ui: AppThemeUI;
}

export interface ThemeGlow {
  primary: string;
  secondary: string;
  tertiary: string;
  halo: string;
}

export function getThemeGlow(theme: AppTheme): ThemeGlow {
  const { terminal, ui } = theme;
  const accent = ui.accent;
  return {
    primary: accent,
    secondary: terminal.blue ?? terminal.cyan ?? accent,
    tertiary: terminal.magenta ?? terminal.yellow ?? accent,
    halo: terminal.green ?? terminal.cyan ?? accent,
  };
}

